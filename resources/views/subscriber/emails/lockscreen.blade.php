# How to set your lock screen

Use the below instructions to set your lock screen with the Found Phone image that was emailed to you.

## iPhone
1. Launch Settings from the Home screen.
2. Tap on Wallpaper.
3. Tap on Choose a New Wallpaper.
4. Tap on the location of the new wallpaper you want to choose.
5. Tap on the lock screen image that you saved.
6. Tap set.
7. Tap Set Lock Screen

## Android
1. Launch Settings from the Home screen.
2. Tap on Wallpaper.
3. Tap on Choose a New Wallpaper.
4. Tap on the location of the new wallpaper you want to choose.
5. Tap on the lock screen image that you saved.
6. Tap set.
7. Tap Set Lock Screen

